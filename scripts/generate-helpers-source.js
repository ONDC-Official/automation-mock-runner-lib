#!/usr/bin/env node
/*
 * Reads src/lib/helpers/default-helpers.js and emits
 * src/lib/helpers/default-helpers-source.ts with the raw source bytes
 * embedded as a template-literal string.
 *
 * This exists because fn.toString() returns whatever a consuming app's
 * bundler has done to the function — minifiers strip comments, lower
 * optional chaining, pack numeric literals. Baking the source as an
 * opaque string keeps it verbatim through any downstream build.
 */

const fs = require("fs");
const path = require("path");

const SRC = path.join(
	__dirname,
	"..",
	"src",
	"lib",
	"helpers",
	"default-helpers.js",
);
const OUT = path.join(
	__dirname,
	"..",
	"src",
	"lib",
	"helpers",
	"default-helpers-source.ts",
);

const raw = fs.readFileSync(SRC, "utf8");

// Drop the trailing `module.exports = { ... };` block — it's for Node's
// CJS loader (tests + aggregator imports), not part of the sandbox bundle.
// The leading `\n+` anchor ensures we don't accidentally match a
// `module.exports` mention inside a comment body (which wouldn't have a
// newline immediately before it).
const stripped = raw
	.replace(/\n+module\.exports\s*=\s*\{[\s\S]*?\};?\s*$/, "")
	.trimEnd();

// Escape for embedding inside a template literal.
// Order matters: backslashes first, then backticks, then ${ sequences.
const escaped = stripped
	.replace(/\\/g, "\\\\")
	.replace(/`/g, "\\`")
	.replace(/\$\{/g, "\\${");

const out = `// AUTO-GENERATED by scripts/generate-helpers-source.js. Do not edit.
// Source: src/lib/helpers/default-helpers.js
/* eslint-disable */
export const DEFAULT_HELPERS_RAW = \`
${escaped}
\`;
`;

fs.writeFileSync(OUT, out, "utf8");
console.log(`wrote ${path.relative(process.cwd(), OUT)}`);
